- (NSString *) uniqueDeviceIdentifierOld
{
    NSString *macaddress = [[UIDevice currentDevice] macaddress];
    NSString *bundleIdentifier = [[NSBundle mainBundle] bundleIdentifier];
    
    NSString *stringToHash = [NSString stringWithFormat:@"%@%@",macaddress,bundleIdentifier];
    NSString *uniqueIdentifier = [stringToHash stringFromMD5];
    
    if (uniqueIdentifier == nil) {
        uniqueIdentifier = @"0";
    }
    
    return uniqueIdentifier;
}

- (NSString *) uniqueDeviceIdentifier
{
    NSString *uniqueIdentifier = @"0";
    
    if ([[UIDevice currentDevice] respondsToSelector:@selector(identifierForVendor)]) {
        uniqueIdentifier = [[UIDevice currentDevice].identifierForVendor UUIDString];
    } else {
        uniqueIdentifier = [self uniqueDeviceIdentifierOld];
    }
    
    return uniqueIdentifier;
}

- (NSString *) vendorIdentifier:(NSString**)oldUdid
{
    NSString *uniqueIdentifier = [self uniqueDeviceIdentifierOld];
    
    if ([[UIDevice currentDevice] respondsToSelector:@selector(identifierForVendor)]) {
        *oldUdid = uniqueIdentifier;
        uniqueIdentifier = [[UIDevice currentDevice].identifierForVendor UUIDString];
    } else {
        *oldUdid = nil;
    }

    return uniqueIdentifier;
}

/*
 * UDID被禁用，我们使用mac地址，并md5 hash一下，得到一个唯一标示符
 */
- (NSString *) uniqueGlobalDeviceIdentifier{
    NSString *macaddress = [[UIDevice currentDevice] macaddress];
    NSString *uniqueIdentifier = [macaddress stringFromMD5];
    //5月21号，根据teddy的反馈，客户端一直启动不了，定位到手机网卡硬件坏掉，所以在这里加上保护
    if (uniqueIdentifier == nil) {
        uniqueIdentifier = @"0";
    }
    return uniqueIdentifier;
}

- (NSString *) platform{
    size_t size;
    sysctlbyname("hw.machine", NULL, &size, NULL, 0);
    char *machine = malloc(size);
    sysctlbyname("hw.machine", machine, &size, NULL, 0);
    NSString *platform = [NSString stringWithCString:machine encoding:NSUTF8StringEncoding];
    free(machine);
    return platform;
}

